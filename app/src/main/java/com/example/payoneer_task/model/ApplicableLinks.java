package com.example.payoneer_task.model;

public class ApplicableLinks {
    private String logo;
    private String self;
    private String lang;
    private String operation;
    private String validation;

    public String getLogo() { return logo; }
    public void setLogo(String value) { this.logo = value; }

    public String getSelf() { return self; }
    public void setSelf(String value) { this.self = value; }

    public String getLang() { return lang; }
    public void setLang(String value) { this.lang = value; }

    public String getOperation() { return operation; }
    public void setOperation(String value) { this.operation = value; }

    public String getValidation() { return validation; }
    public void setValidation(String value) { this.validation = value; }
}
